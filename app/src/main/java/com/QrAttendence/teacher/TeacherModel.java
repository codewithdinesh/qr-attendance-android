package com.QrAttendence.teacher;

public class TeacherModel {
    public String str_name, str_email, str_clas, str_phone_no, str_id, str_pass;

    public TeacherModel(String str_name, String str_email, String str_clas, String str_phone_no, String str_id, String str_pass) {
        this.str_name = str_name;
        this.str_email = str_email;
        this.str_clas = str_clas;
        this.str_phone_no = str_phone_no;
        this.str_id = str_id;
        this.str_pass = str_pass;
    }
}
