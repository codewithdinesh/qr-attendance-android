package com.QrAttendence.teacher;

public class LectureModel {
    public String s_subject, s_date, s_time, s_end_time, s_code, id;

    public LectureModel(String s_subject, String s_date, String s_time, String s_end_time, String s_code, String id) {
        this.s_subject = s_subject;
        this.s_date = s_date;
        this.s_time = s_time;
        this.s_end_time = s_end_time;
        this.s_code = s_code;
        this.id = id;
    }


}
